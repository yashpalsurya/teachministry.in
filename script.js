  function copyShayri(text, button) {
    // Create a temporary textarea element to copy the text to clipboard
    const tempTextArea = document.createElement("textarea");
    tempTextArea.value = text;
    document.body.appendChild(tempTextArea);
    tempTextArea.select();
    document.execCommand("copy");
    document.body.removeChild(tempTextArea);
  
    // Change button text temporarily to indicate copy
    button.textContent = "Shayri Copied!";
    button.style.backgroundColor = "green"; // Change button color to green
    setTimeout(function() {
      button.textContent = "Copy Shayri";
      button.style.backgroundColor = ""; // Reset button color
    }, 2000); // Reset button text after 2 seconds
  }
  
